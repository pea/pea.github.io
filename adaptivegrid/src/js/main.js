[...document.querySelectorAll('.code')].forEach((item) => {
    item.innerHTML = htmlEscape(item.innerHTML);
});

function htmlEscape(str) {
    return str
        .replace(/&/g, '&amp;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#39;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;');
}
